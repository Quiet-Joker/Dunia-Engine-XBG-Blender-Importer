# ---------------------------------------------------------------------------
# Editing helpers
# ---------------------------------------------------------------------------

_COLOR_FLOOR = 0.001   # see _safe_color


def _safe_color(rgb):
    """Clamp every component up to _COLOR_FLOOR so the shader's normalize /
    reciprocal / pow() paths don't produce NaN/Inf from literal (0,0,0).

    0.001 sits below the 8-bit display floor (1/255 ≈ 0.0039) so the result
    is visually pitch-black, but mathematically safe."""
    return tuple(max(float(x), _COLOR_FLOOR) for x in rgb[:3])


def _resolve_slot(slot_key, textures):
    """Map a template's g0 slot name onto one of the user's baked maps.

    Returns the resolved path string, or None to keep the template's
    original (real game) path verbatim — same fallback the engine uses
    when materials share a texture across slots.  Never returns ''.

    `textures` keys are the canonical-bake names:
        DiffuseTexture1, NormalTexture1, SpecularTexture1, IlluminationTexture
    """
    d = textures.get('DiffuseTexture1')
    n = textures.get('NormalTexture1')
    s = textures.get('SpecularTexture1')
    i = textures.get('IlluminationTexture')
    k = slot_key.lower()
    if 'normal' in k:
        return n
    if 'illumination' in k or 'glow' in k:
        return i or d
    if 'specular' in k:    # also matches SpecularID
        return s
    if ('diffuse' in k or 'burnt' in k or 'mask' in k
            or 'reflection' in k or 'albedo' in k):
        return d
    return None


def _set_scalar(m, key, value):
    """Update an existing (key, value) entry in any group of the parsed material.

    Group is auto-selected from value's type / arity:
        int       → g5
        float     → g1
        (f,)      → g1
        (f, f)    → g2
        (f, f, f) → g3
        (f, f, f, f) → g4

    Does NOT append — if the template's schema doesn't already contain `key`,
    the call is silently skipped.  This preserves the engine-validated schema
    (adding unknown keys to a non-illumination Generic, for example, would
    crash the loader).
    """
    if isinstance(value, bool) or (isinstance(value, int) and not isinstance(value, float)):
        gi, vals = 5, (int(value) & 0xFFFFFFFF,)
    else:
        seq = tuple(float(x) for x in value) if isinstance(value, (list, tuple)) \
              else (float(value),)
        gi = {1: 1, 2: 2, 3: 3, 4: 4}.get(len(seq))
        if gi is None:
            return False
        vals = seq
    for idx, (k, _v) in enumerate(m['groups'][gi]):
        if k == key:
            m['groups'][gi][idx] = (key, vals)
            return True
    return False


def _set_color(m, key, rgb):
    """Set a color parameter that the template may store as float3 (g3) or
    float4 (g4).  Tries g3 first, then g4 (with alpha=1.0 padding)."""
    rgb_safe = _safe_color(rgb)
    for idx, (k, _v) in enumerate(m['groups'][3]):
        if k == key:
            m['groups'][3][idx] = (k, rgb_safe)
            return True
    rgba = rgb_safe + (1.0,)
    for idx, (k, _v) in enumerate(m['groups'][4]):
        if k == key:
            m['groups'][4][idx] = (k, rgba)
            return True
    return False


def _set_emissive(m, power, color, always_on, template_type):
    """Override the emission-strength parameter that this template exposes.

    Unlit               -> HDRMul (g1, float1)
    Generic/Flesh/Cloth -> IlluminationColor1 (g4, float4 RGBA)
    Leaf/BigLeaf        -> IlluminationColor1 (g4, float4 RGBA)

    For float4 illumination, alpha controls the day/night blend:
        always_on=True  → alpha = 0.0  (constant glow)
        always_on=False → alpha = 1.0  (night-only, scales with BioLightIntensity)
    """
    if template_type == 'Unlit':
        _set_scalar(m, 'HDRMul', float(power))
        return
    alpha = 0.0 if always_on else 1.0
    if color is not None:
        r, g, b = _safe_color(color)
    else:
        r = g = b = float(power)
    rgba = (r, g, b, alpha)
    # IlluminationColor1 is always float4 in the templates we embed.
    for idx, (k, _v) in enumerate(m['groups'][4]):
        if k == 'IlluminationColor1':
            m['groups'][4][idx] = (k, rgba)
            return


# ---------------------------------------------------------------------------
# Serialiser — rebuilds the .xbm bytes from a parsed/edited dict.
# ---------------------------------------------------------------------------

def _build_payload(m):
    out = bytearray(m['preamble'])
    out += _wr_str(m['name'])
    out += _wr_str(m['template'])
    for gi, (kind, ncomp) in enumerate(_PARSE_GROUPS):
        entries = m['groups'][gi]
        out += struct.pack('<I', len(entries))
        for ent in entries:
            if kind == 'str':
                key, val = ent
                out += _wr_str(val)
                out += _wr_str(key)
            else:
                key, vals = ent
                out += _wr_str(key)
                if kind == 'f':
                    out += struct.pack('<%df' % ncomp,
                                       *[float(x) for x in vals])
                else:
                    out += struct.pack('<I', int(vals[0]) & 0xFFFFFFFF)
    out += m['trailing']
    return bytes(out)


def serialize(m):
    """Reassemble the full .xbm file bytes from a parsed/edited dict.

    Preserves everything except the LTMD payload (and the file-header
    size field at 0x14, which is re-patched to match the new size)."""
    d = m['file']
    idx = m['idx']
    payload = _build_payload(m)
    data_size = len(payload)
    chunk_size = 20 + data_size
    chunk = (b'LTMD'
             + struct.pack('<4I', m['ver'], chunk_size, data_size, m['rsv'])
             + payload)
    head = bytearray(d[:idx])
    tail = d[idx + m['chunk_size']:]
    new = head + chunk + tail
    struct.pack_into('<I', new, 0x14, len(new) - 12)
    return bytes(new)


