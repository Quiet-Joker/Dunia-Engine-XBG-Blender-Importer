def build_xbm(template_type, name, textures,
              diffuse_color=(0.996, 0.996, 0.996),
              specular_color=None,
              specular_power=20.0,
              emissive_power=0.0,
              emissive_color=None,
              emissive_always_on=True,
              alpha_mode='KEEP',
              two_sided=None,
              vertex_color=None,
              unlit_blending=None):
    """Construct a complete .xbm file (bytes) by cloning a real game template.

    template_type      : one of TEMPLATE_TYPES
    name               : material name written into the LTMD (forced lowercase)
    textures           : dict of canonical baked-map paths, e.g.
                         {'DiffuseTexture1': r'graphics\\\\...\\\\foo_d.xbt',
                          'NormalTexture1':  r'graphics\\\\...\\\\foo_n.xbt',
                          'SpecularTexture1':r'graphics\\\\...\\\\foo_s.xbt',
                          'IlluminationTexture': r'graphics\\\\...\\\\foo_m.xbt'}
                         Slots the user didn't bake get the template's
                         original (real game) path — keeps the schema
                         valid and visually inert via zero color masks.
    diffuse_color      : (r, g, b) tint of DiffuseColor1 / DiffuseColorBase
    specular_color     : (r, g, b) tint of SpecularColor1 / SpecularColorBase
                         (None = keep the template's value)
    specular_power     : Blinn-Phong shininess written to SpecularPower
    emissive_power     : >0 enables emission (HDRMul for Unlit, else
                         picks the illumination variant of the template)
    emissive_color     : (r, g, b) tint for IlluminationColor1; None =>
                         (power, power, power) i.e. white emission
    emissive_always_on : True  -> IlluminationColor1.a = 0.0 (constant)
                         False -> alpha = 1.0 (night-only BioLightIntensity)
    alpha_mode         : 'KEEP' (default) preserves the template's alpha
                         flags; 'NONE' / 'TEST' / 'BLEND' overrides them
    two_sided          : None = keep template; bool overrides TwoSided
    vertex_color       : None = keep template; bool overrides VertexColorEnabled
    unlit_blending     : Unlit only — 'OPAQUE' / 'ADDITIVE' / 'MULTIPLY' or
                         None to keep the template's BlendingType
    """
    # Pick template variant: with-illumination iff caller asked for emission
    # and the template family supports it (skipped for Unlit which uses HDRMul,
    # and for templates with no illumination variant in the corpus).
    want_illum = (emissive_power > 0 and template_type != 'Unlit')
    blob = _TEMPLATE_BLOBS.get((template_type, want_illum))
    if blob is None and want_illum:
        # No illumination variant for this template — fall back to non-illum.
        # Emission set call below will silently no-op since IlluminationColor1
        # isn't in this template's schema.
        blob = _TEMPLATE_BLOBS.get((template_type, False))
    if blob is None:
        # Unknown template — fall back to Generic.
        _VL.log(f"[xbm_builder] unknown template {template_type!r}, using Generic")
        blob = _TEMPLATE_BLOBS[('Generic', want_illum)]

    _VL.log(f"\n[xbm_builder.build_xbm] ---- {name} ----")
    _VL.log(f"  template       : {template_type}  (illum_variant={want_illum})")
    _VL.log(f"  textures (in)  : {textures}")
    _VL.log(f"  diffuse_color  : {diffuse_color}  specular_color={specular_color}")
    _VL.log(f"  specular_power : {specular_power}")
    _VL.log(f"  emissive_power : {emissive_power}  color={emissive_color}  always_on={emissive_always_on}")
    _VL.log(f"  alpha_mode     : {alpha_mode}  two_sided={two_sided}  vertex_color={vertex_color}")
    _VL.log(f"  unlit_blending : {unlit_blending}")

    m = parse_dmtl(blob)
    m['name'] = str(name).lower()

    # g0 textures — replace each slot with our baked map, or keep the
    # template's original game path if we have no bake for that slot.
    new_g0 = []
    for key, old_val in m['groups'][0]:
        repl = _resolve_slot(key, textures)
        new_g0.append((key, repl if repl else old_val))
    m['groups'][0] = new_g0

    # Colors (zero-color-safe).
    _set_color(m, 'DiffuseColor1', diffuse_color)
    _set_color(m, 'DiffuseColorBase', diffuse_color)
    if specular_color is not None:
        _set_color(m, 'SpecularColor1', specular_color)
        _set_color(m, 'SpecularColorBase', specular_color)

    # SpecularPower (float1).
    _set_scalar(m, 'SpecularPower', float(specular_power))

    # Emission.
    if emissive_power > 0:
        _set_emissive(m, emissive_power, emissive_color, emissive_always_on,
                      template_type)

    # Alpha mode override.
    if alpha_mode == 'NONE':
        _set_scalar(m, 'AlphaTestEnabled', 0)
        _set_scalar(m, 'AlphaBlendEnabled', 0)
    elif alpha_mode == 'TEST':
        _set_scalar(m, 'AlphaTestEnabled', 1)
        _set_scalar(m, 'AlphaBlendEnabled', 0)
    elif alpha_mode == 'BLEND':
        _set_scalar(m, 'AlphaTestEnabled', 0)
        _set_scalar(m, 'AlphaBlendEnabled', 1)
    # alpha_mode == 'KEEP' — leave the template's value alone

    if two_sided is not None:
        _set_scalar(m, 'TwoSided', int(bool(two_sided)))
    if vertex_color is not None:
        _set_scalar(m, 'VertexColorEnabled', int(bool(vertex_color)))
    if unlit_blending is not None and template_type == 'Unlit':
        bt = {'OPAQUE': 0, 'ADDITIVE': 6, 'MULTIPLY': 7}.get(unlit_blending, 0)
        _set_scalar(m, 'BlendingType', bt)

    _VL.log(f"  g0 textures    : {m['groups'][0]}")
    _VL.log(f"  g1 float1      : {m['groups'][1]}")
    _VL.log(f"  g3 float3      : {m['groups'][3]}")
    _VL.log(f"  g4 float4      : {m['groups'][4]}")
    _VL.log(f"  g5 int         : {m['groups'][5]}")

    out = serialize(m)
    _VL.log(f"  total file     : {len(out)}B")

    # Post-build self-validation: re-parse the bytes we just wrote and
    # diff them against the in-memory dict.  Catches truncated strings,
    # corrupted preambles, mis-typed group entries, and any serializer
    # bug that produces structurally invalid XBM bytes BEFORE the file
    # gets to the game.  Issues are logged to both verbose log and JSONL
    # (event=xbm_validate_fail) but do NOT raise — the caller decides
    # whether to ship the bytes or bail.
    issues = _validate_serialized(m, out, label=str(name))
    if _TL is not None:
        import hashlib as _hl
        _TL.kvblock(
            f"xbm_builder.build_xbm result '{name}'",
            [
                ("name",            m['name']),
                ("template",        m['template']),
                ("template_type",   template_type),
                ("illum_variant",   want_illum),
                ("bytes",           len(out)),
                ("sha1",            _hl.sha1(out).hexdigest()),
                ("g0_textures",     [(k, v) for (k, v) in m['groups'][0]]),
                ("g1_float1",       [(k, v) for (k, v) in m['groups'][1]]),
                ("g3_float3",       [(k, v) for (k, v) in m['groups'][3]]),
                ("g4_float4",       [(k, v) for (k, v) in m['groups'][4]]),
                ("g5_int",          [(k, v) for (k, v) in m['groups'][5]]),
                ("validation_ok",   not issues),
                ("validation_issues", issues),
            ],
            tier="DEBUG", event="xbm_build_result")
    _VL.log(f"[xbm_builder.build_xbm] ---- {name} done"
            + (" (VALIDATION FAILED)" if issues else "")
            + " ----\n")
    return out


# ---------------------------------------------------------------------------
# Low-level interface for the standalone XBM editor.
# ---------------------------------------------------------------------------

def serialize_xbm(name, template, groups):
    """Build an .xbm with arbitrary (key, value) entries from scratch.

    `groups` is {'g0': [(key, value), ...], ..., 'g5': [...]}.  Used by
    the editor where the user manually adds/edits any parameter.  No
    schema validation — whatever the caller passes is written verbatim.

    Uses the Generic-no-illum template's peripheral chunks (EDON, DIKS,
    DNKS, SDOL, etc.) as a wrapper, since those are byte-identical across
    every real game .xbm.
    """
    # Parse the Generic-no-illum template to get peripheral chunk bytes.
    base = parse_dmtl(_TEMPLATE_BLOBS[('Generic', False)])
    base['name'] = str(name)
    base['template'] = str(template)
    base['groups'] = [list(groups.get('g%d' % i, [])) for i in range(6)]
    return serialize(base)
