"""XBM material builder — clone-based.

Builds a valid .xbm by cloning a real, in-game-tested game material of
the chosen template type (and the variant that does or does not include
illumination) and then overwriting only the user-controlled fields:

    name              | material name written into the LTMD
    g0 texture paths  | DiffuseTexture1 / NormalTexture1 / SpecularTexture1
                      | / IlluminationTexture / DiffuseTexture2 / MaskTexture1
                      | / etc.  Slots we have no bake for keep the template's
                      | original (real game) path — same trick the engine uses
                      | for stock materials that share textures.
    DiffuseColor*     | RGB tint of the diffuse map
    SpecularColor*    | RGB tint of the specular map (optional)
    SpecularPower     | Blinn-Phong shininess exponent
    IlluminationColor1| RGBA emission (Generic/Flesh/Cloth/etc.; alpha = day/night)
    HDRMul            | Unlit-only brightness multiplier
    AlphaTest/Blend   | optional override of the alpha mode
    TwoSided          | optional override
    VertexColor*      | optional override
    BlendingType      | Unlit-only blend mode (opaque/additive/multiply)

Everything else — the schema (which keys exist in each group, in which
order), the preamble, the trailing bytes, and ALL peripheral chunks
(EDON, DIKS, DNKS, SDOL, XOBB, HPSB, PMCP, PMCU) — is preserved
byte-for-byte from the source template.  The Dunia engine validates the
parameter list against known-good schemas per template, and uses the
SDOL chunk for per-material LOD slot binding on save reload, so the
only reliable way to stay engine-valid across all template types is to
NOT regenerate any of that from scratch.

The 14 template blobs are embedded below — no external lookup needed.
Each is a real Avatar game .xbm chosen as the canonical representative
of its (template_type, illumination?) variant from a 1582-file corpus
survey.  See _TEMPLATE_BLOBS source comments for each filename.
"""

import struct

try:
    from ..Core.debug import VerboseLogger as _VL
except Exception:
    class _VL:
        @staticmethod
        def log(m): pass

try:
    from ..Core.debug import TraceLogger as _TL
except Exception:
    _TL = None


def _validate_serialized(original_dict, serialized_bytes, label):
    """Round-trip-parse `serialized_bytes` and compare against `original_dict`.

    Catches structural divergence between what build_xbm THOUGHT it was
    writing and what the bytes actually decode to.  A malformed length
    prefix, a corrupted preamble, a group entry that overruns its kind →
    re-parse fails or fields disagree.  Any divergence is logged at INFO
    via TraceLogger (event `xbm_validate_fail`) and as a VerboseLogger
    warn so it shows up even with Trace logging off.

    Returns the list of issues (empty = clean).  Does NOT raise — the
    caller decides whether to reject the file or write it anyway with
    the warning attached.
    """
    issues = []
    try:
        reparsed = parse_dmtl(serialized_bytes)
    except Exception as exc:
        issues.append(f"reparse failed: {exc.__class__.__name__}: {exc}")
        _emit_validation_failure(label, issues, original_dict, None)
        return issues

    if reparsed['name'] != original_dict['name']:
        issues.append(
            f"name mismatch  wrote={original_dict['name']!r}  "
            f"read={reparsed['name']!r}")
    if reparsed['template'] != original_dict['template']:
        issues.append(
            f"template mismatch  wrote={original_dict['template']!r}  "
            f"read={reparsed['template']!r}")

    for gi in range(len(original_dict['groups'])):
        wrote = original_dict['groups'][gi]
        read  = reparsed['groups'][gi]
        if len(wrote) != len(read):
            issues.append(
                f"group{gi} count mismatch  wrote={len(wrote)}  read={len(read)}")
            continue
        for ei, (wk, wv) in enumerate(wrote):
            rk, rv = read[ei]
            if wk != rk:
                issues.append(
                    f"group{gi}[{ei}] key mismatch  wrote={wk!r}  read={rk!r}")
                continue
            # Float comparisons: allow tiny FP drift from pack/unpack.
            if isinstance(wv, (tuple, list)):
                if (not isinstance(rv, (tuple, list))
                        or len(wv) != len(rv)
                        or any(abs(float(a) - float(b)) > 1e-6
                               for a, b in zip(wv, rv))):
                    issues.append(
                        f"group{gi}[{ei}] {wk!r} value mismatch  "
                        f"wrote={wv}  read={rv}")
            else:
                # str entry
                if wv != rv:
                    issues.append(
                        f"group{gi}[{ei}] {wk!r} value mismatch  "
                        f"wrote={wv!r}  read={rv!r}")

    # Trailing bytes must round-trip exactly — those are peripheral chunks
    # (SDOL, XOBB, …) we copy verbatim from the template; any difference
    # means the serializer corrupted them.
    if bytes(original_dict.get('trailing', b'')) != bytes(reparsed.get('trailing', b'')):
        issues.append(
            f"trailing bytes differ  wrote={len(original_dict.get('trailing', b''))}B  "
            f"read={len(reparsed.get('trailing', b''))}B")

    if issues:
        _emit_validation_failure(label, issues, original_dict, reparsed)
    return issues


def _emit_validation_failure(label, issues, wrote_dict, read_dict):
    """Surface validation failures both to the verbose log and to the JSONL."""
    _VL.log(f"\n[xbm_builder.validate] '{label}' FAILED ({len(issues)} issue(s)):")
    for it in issues:
        _VL.log(f"    {it}")
    if _TL is not None:
        try:
            _TL.info(
                f"[xbm_builder] XBM '{label}' self-validation FAILED",
                event="xbm_validate_fail",
                data={
                    "label":     label,
                    "issues":    issues,
                    "wrote_name":     wrote_dict.get('name') if wrote_dict else None,
                    "wrote_template": wrote_dict.get('template') if wrote_dict else None,
                    "wrote_group_counts": (
                        [len(g) for g in wrote_dict['groups']]
                        if wrote_dict else None),
                    "read_group_counts": (
                        [len(g) for g in read_dict['groups']]
                        if read_dict else None),
                })
        except Exception:
            pass


# ---------------------------------------------------------------------------
# DMTL parser — also used by the editor / round-trip tests.
# ---------------------------------------------------------------------------

def _rd_str(d, p):
    n = struct.unpack_from('<I', d, p)[0]
    p += 4
    s = d[p:p + n]
    p += n
    if p < len(d) and d[p] == 0:
        p += 1
    return s.decode('latin-1'), p


def _wr_str(s):
    b = str(s).encode('latin-1')
    return struct.pack('<I', len(b)) + b + b'\x00'


_PARSE_GROUPS = (('str', 0), ('f', 1), ('f', 2), ('f', 3), ('f', 4), ('i', 1))


def parse_dmtl(d):
    """Parse a complete XBM into an editable dict.

    Returns: {file, idx, chunk_size, ver, rsv, preamble, name, template,
              groups: [g0..g5], trailing}
    where each group is a list of (key, value) tuples.
    """
    idx = d.find(b'LTMD')
    if idx < 0:
        raise ValueError("no LTMD chunk")
    ver, chunk_size, data_size, rsv = struct.unpack_from('<4I', d, idx + 4)
    pay0 = idx + 20
    payload = d[pay0:pay0 + data_size]

    pre = None
    for off in (5, 9, 1, 0):
        if off + 4 > len(payload):
            continue
        ln = struct.unpack_from('<I', payload, off)[0]
        if 0 < ln < 64 and off + 4 + ln <= len(payload):
            body = payload[off + 4:off + 4 + ln]
            if all(32 <= c < 127 for c in body):
                pre = off
                break
    if pre is None:
        raise ValueError("LTMD preamble not found")
    preamble = payload[:pre]

    p = pre
    name, p = _rd_str(payload, p)
    template, p = _rd_str(payload, p)

    groups = []
    for kind, ncomp in _PARSE_GROUPS:
        count = struct.unpack_from('<I', payload, p)[0]
        p += 4
        entries = []
        for _ in range(count):
            if kind == 'str':
                val, p = _rd_str(payload, p)
                key, p = _rd_str(payload, p)
                entries.append((key, val))
            else:
                key, p = _rd_str(payload, p)
                if kind == 'f':
                    vals = struct.unpack_from('<%df' % ncomp, payload, p)
                    p += 4 * ncomp
                else:
                    vals = (struct.unpack_from('<I', payload, p)[0],)
                    p += 4
                entries.append((key, vals))
        groups.append(entries)

    return {
        'file': d, 'idx': idx, 'chunk_size': chunk_size,
        'ver': ver, 'rsv': rsv, 'preamble': preamble,
        'trailing': payload[p:],
        'name': name, 'template': template,
        'groups': [list(g) for g in groups],
    }


